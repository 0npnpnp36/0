const lines = art.split(/\r?\n/).filter((l) => l.length)
const rows = lines.length
const cols = Math.max(...lines.map((l) => [...l].length))
const cellW = 2
const cellH = 4
const pad = 2
const W = cols * cellW + pad * 2
const H = rows * cellH + pad * 2
const rects = []

for (let y = 0; y < rows; y++) {
  const chars = [...lines[y]]
  for (let x = 0; x < chars.length; x++) {
    const cp = chars[x].codePointAt(0)
    if (cp < 0x2800 || cp > 0x28ff) continue
    const v = cp - 0x2800
    const dots = [
      [0, 0, 1],
      [0, 1, 2],
      [0, 2, 4],
      [1, 0, 8],
      [1, 1, 16],
      [1, 2, 32],
      [0, 3, 64],
      [1, 3, 128],
    ]
    for (const [dx, dy, bit] of dots) {
      if (v & bit) {
        const px = pad + x * cellW + dx
        const py = pad + y * cellH + dy
        rects.push(
          `<rect x="${px}" y="${py}" width="1" height="1" fill="#fff"/>`,
        )
      }
    }
  }
}

const svg = `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 ${W} ${H}" shape-rendering="crispEdges">
<rect width="${W}" height="${H}" fill="#000"/>
${rects.join('\n')}
</svg>
`

fs.writeFileSync('public/favicon.svg', svg)
